type MessageKey = keyof typeof messages.en;

export function useTranslation() {
    const { auth } = usePage<{ auth: Auth }>().props;
    const locale = auth.user?.locale ?? 'en';

    return {
        locale,
        t: (key: MessageKey) => messages[locale]?.[key] ?? messages.en[key],
    };
}
